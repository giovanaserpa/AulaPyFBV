a=10
b=3

#Adição
soma = a+b
print('Adição:', soma)

#Subtração
subtracao = a-b
print('Subtração:', subtracao)

#Multiplicação
multiplicacao = a*b
print('Multiplicação:', multiplicacao)

#Divisão (float)
divisao = a/b
print('Divisão:', divisao)

#Divisão inteira
divisao_inteira = a // b
print('Divisão inteira:', divisao_inteira)

#Módulo (resto da divisão)
modulo = a%b
print('Modulo:', modulo)

#Exponenciação (potência)
potencia = a**b
print('Potência:', potencia)